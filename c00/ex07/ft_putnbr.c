#include <unistd.h>

void	ft_putnbr(int nb);

void	ft_putnbr(int nb)
{
	int a;
	int b;

	a = 0;
	b = 45;
	if (nb < 0)
	{
		write(1, &b, 1);
		nb = nb * -1;
	}
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
	}
	a = nb % 10 + 48;
	write(1, &a, 1);
}
