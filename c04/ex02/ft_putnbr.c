#include <unistd.h>

void	ft_putnbr(int nb);

void	ft_putnbr(int nb)
{
	int a;
	int b;

	a = 0;
	b = 45;
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
	}
	else if (nb < 0)
	{
		nb = nb * -1;
		write(1, &b, 1);
	}
	if (nb >= 10)
		ft_putnbr(nb / 10);
	if (!(nb == -2147483648))
	{
		a = nb % 10 + 48;
		write(1, &a, 1);
	}
}
