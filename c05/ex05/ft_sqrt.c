int		ft_sqrt(int nb);

int		ft_sqrt(int nb)
{
	int i;

	i = 1;
	if (nb > 2147395600)
		return (0);
	while (nb / i > i)
	{
		i++;
	}
	if (nb == 1)
	{
		return (1);
	}
	else if (i == nb)
	{
		return (0);
	}
	else if (i * i == nb)
	{
		return (i);
	}
	return (0);
}
