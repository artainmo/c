#include <unistd.h>

int		main(int argc, char **argv)
{
	int i;
	int l;

	l = 0;
	i = 1;
	while (i < argc)
	{
		while (argv[i][l])
		{
			write(1, &argv[i][l], 1);
			l++;
		}
		write(1, "\n", 1);
		l = 0;
		i++;
	}
	return (0);
}
