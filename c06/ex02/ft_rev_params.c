#include <unistd.h>

int		main(int argc, char **argv)
{
	int i;
	int l;

	l = 0;
	i = argc - 1;
	while (i > 0)
	{
		while (argv[i][l] != '\0')
		{
			write(1, &argv[i][l], 1);
			l++;
		}
		write(1, "\n", 1);
		i--;
		l = 0;
	}
}
